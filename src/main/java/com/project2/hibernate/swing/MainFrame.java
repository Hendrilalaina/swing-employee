    public void loadTable2() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        List<Employe> employes = new EmployeController().lister();
        for( Employe employe : employes) {
            model.addRow(new Object[]{employe.getCodeEmp(), employe.getNom(), employe.getPrenom(), employe.getPoste()});
        }
    }
    
    public void loadTable3() {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);
        List<Lieu> lieux = new LieuController().lister();
        for(Lieu lieu : lieux) {
            model.addRow(new Object[]{lieu.getCodeLieu(), lieu.getDesignation(), lieu.getProvince()});
        }
    }

    public void loadTable4() {
        DefaultTableModel model = (DefaultTableModel) jTable4.getModel();
        model.setRowCount(0);
        List<Affecter> affecters = new AffecterController().lister();
        SimpleDateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
        for(Affecter affecter : affecters) {
            model.addRow(new Object[] {affecter.getId().getEmploye().getCodeEmp(), affecter.getId().getLieu().getDesignation(), formater.format(Date.from(affecter.getDate().atStartOfDay(ZoneId.systemDefault()).toInstant()))});
        }
    }
