    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        String codeEmp = (String) jTable4.getValueAt(jTable4.getSelectedRow(), 0);
        String designation = (String) jTable4.getValueAt(jTable4.getSelectedRow(), 1);
        Lieu lieu = new LieuController().lieuByDesignation(designation);
        
        int check = JOptionPane.showConfirmDialog(this, "Supprimez cette affectation?");
        if(check == 0) {
            AffecterController affecterController = new AffecterController();
            try {
                affecterController.supprimer(codeEmp, lieu.getCodeLieu());
                JOptionPane.showMessageDialog(this, "L' affectation est supprimée.", "Succès", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Une erreur s'est produite", "Erreur",JOptionPane.ERROR_MESSAGE);
            }
        }
        loadTable4();
        
    }//GEN-LAST:event_jButton10ActionPerformed

