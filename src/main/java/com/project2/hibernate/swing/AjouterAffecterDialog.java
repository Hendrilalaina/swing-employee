	private void ajouterBoutonAjouterAffecterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterBoutonAjouterAffecterActionPerformed
            String codeEmploye = (String) codeEmployeComboAjouterAffecter.getSelectedItem();
            String designation = (String) codeLieuComboAjouterAffecter.getSelectedItem();
            Lieu lieu = new LieuController().lieuByDesignation(designation);
            Date date = dateAjouterAffecter.getDate();
       
            if (!codeEmploye.equals("") && !designation.equals("") && !date.toString().equals("")) {
                AffecterController affecterController = new AffecterController();
                try {
                    affecterController.ajouter(codeEmploye, lieu.getCodeLieu(), date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
                    this.setVisible(false);
                    JOptionPane.showMessageDialog(this.getParent(), "L'affectaion est ajoutée.");
                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "L'affectation est déjà existée!", "Error",HEIGHT);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Compléter tous les champs!");
            }
        
    }//GEN-LAST:event_ajouterBoutonAjouterAffecterActionPerformed

