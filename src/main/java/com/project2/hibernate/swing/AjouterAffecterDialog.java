    public void loadComboBox() {
    	EmployeController employeController = new EmployeController();
    	LieuController lieuController = new LieuController();
    	
    	List<Employe> employes = employeController.lister();
    	List<Lieu> lieux = lieuController.lister();
    	codeEmployeComboAjouterAffecter.removeAllItems();
        codeEmployeComboAjouterAffecter.addItem("");
    	for (Employe employe : employes) {
    		codeEmployeComboAjouterAffecter.addItem(employe.getCodeEmp());
    	}
    	codeLieuComboAjouterAffecter.removeAllItems();
        codeLieuComboAjouterAffecter.addItem("");
    	for (Lieu lieu : lieux) {
    		codeLieuComboAjouterAffecter.addItem(lieu.getDesignation());
    	}
    	
    }

